package lesson1BasicOutput;

public class BasicOutput {
    //1. Getting started
    //makes program run, won't run without
    public static void main(String[] args) {

        //Will print same line
        System.out.print("Hello world");
        System.out.print("I like pizza");

        // \n also a breakpoint for new line in abov

        //Will print on separate lines
        System.out.println("Hello world");
        System.out.println("I like pizza");

        // SOUT then TAB = shortcut for system.out.p
        System.out.println();
    }
}